// ===========================================================================
// Test: Frame Data Integrity
//
// Verifies that frame pixel data is actually meaningful (not just random
// noise or repeated patterns).  Decodes a known test pattern and checks
// that the BGR data has reasonable statistics.
// ===========================================================================

TEST(VideoPipelineE2E, FrameDataIntegrity_NonTrivialPixels)
{
    shm_unlink("/oe.vid.ingest");  // Clean stale segment

    // Use a distinct filename — ShmHeaderResolutionCorrect also generates a
    // 720p30 clip but at 2 s duration.  Sharing one name causes the file
    // content to depend on test execution order.
    const auto videoPath = kTestDataDir / "test_720p30_3s.mp4";
    if (!ensureTestVideo(videoPath, 1280, 720, 30, 3)) {
        GTEST_SKIP() << "Could not generate test video (ffmpeg not available)";
    }

    // Configure with smaller resolution for faster processing
    VideoIngestNode::Config cfg;
    cfg.v4l2Device      = videoPath.string();
    cfg.frameWidth      = 640;
    cfg.frameHeight     = 480;
    cfg.pubPort         = 25755;  // different port to avoid clash
    cfg.wsBridgeSubPort = 25770;
    cfg.moduleName      = "video_ingest_integrity_test";

    VideoIngestNode node(cfg);
    auto tr = node.configureTransport();
    ASSERT_TRUE(tr.has_value()) << tr.error();
    auto lr = node.loadInferencer();
    ASSERT_TRUE(lr.has_value()) << lr.error();

    const std::size_t frameBytes = 640u * 480u * 3u;
    constexpr uint32_t kSlots = kCircularBufferSlotCount;

    ShmCircularBuffer<ShmVideoHeader> consumer(
        "/oe.vid.ingest", kSlots, frameBytes, false);

    node.onBeforeRun();

    // Wait for at least one frame
    std::vector<uint8_t> capturedFrame(frameBytes, 0);
    bool gotFrame = false;
    const auto deadline = Clock::now() + std::chrono::seconds(5);

    while (Clock::now() < deadline) {
        auto slot = consumer.readLatestSlot();
        if (slot.valid && consumer.header()->seqNumber > 0) {
            std::memcpy(capturedFrame.data(), slot.data, frameBytes);
            gotFrame = true;
            SPDLOG_INFO("captured_frame: seq={}, slot={}",
                consumer.header()->seqNumber, slot.slot);
            break;
        }
        std::this_thread::sleep_for(std::chrono::milliseconds(10));
    }

    node.onAfterStop();
    shm_unlink("/oe.vid.ingest");  // Clean up

    ASSERT_TRUE(gotFrame) << "No frame received within 5 seconds";

    // Analyze pixel statistics
    uint64_t sum = 0;
    uint32_t uniqueValues = 0;
    bool seenNonZero = false;
    std::array<bool, 256> valuesSeen{};  // zero-initialized (all false)

    for (std::size_t i = 0; i < frameBytes; ++i) {
        const uint8_t v = capturedFrame[i];
        sum += v;
        if (!valuesSeen[v]) {
            valuesSeen[v] = true;
            uniqueValues++;
        }
        if (v != 0) seenNonZero = true;
    }

    const double avgPixel = static_cast<double>(sum) / static_cast<double>(frameBytes);

    SPDLOG_INFO("frame_stats: avg_pixel={:.1f}, unique_values={}, non_zero={}",
        avgPixel, uniqueValues, seenNonZero);

    // Frame should not be all black
    EXPECT_TRUE(seenNonZero) << "Frame is entirely black (all zeros)";

    // A test pattern should have diverse pixel values (at least 10 unique)
    EXPECT_GT(uniqueValues, 10u)
        << "Frame has too few unique pixel values — likely corrupt";

    // Average pixel value should be somewhere reasonable (not all 0 or all 255)
    EXPECT_GT(avgPixel, 10.0) << "Average pixel value suspiciously low";
    EXPECT_LT(avgPixel, 245.0) << "Average pixel value suspiciously high";
}

// ===========================================================================
// Test: SHM Header Consistency
//
// Verifies that the SHM video header is correctly populated with the
// configured resolution after pipeline startup.
// ===========================================================================

TEST(VideoPipelineE2E, ShmHeaderResolutionCorrect)
{
    shm_unlink("/oe.vid.ingest");  // Clean stale segment

    const auto videoPath = kTestDataDir / "test_720p30_2s.mp4";
    if (!ensureTestVideo(videoPath, 1280, 720, 30, 2)) {
        GTEST_SKIP() << "Could not generate test video";
    }

    VideoIngestNode::Config cfg;
    cfg.v4l2Device      = videoPath.string();
    cfg.frameWidth      = 1280;
    cfg.frameHeight     = 720;
    cfg.pubPort         = 25855;
    cfg.wsBridgeSubPort = 25870;
    cfg.moduleName      = "video_ingest_hdr_test";

    VideoIngestNode node(cfg);
    auto tr = node.configureTransport();
    ASSERT_TRUE(tr.has_value()) << tr.error();
    auto lr = node.loadInferencer();
    ASSERT_TRUE(lr.has_value()) << lr.error();

    const std::size_t frameBytes = 1280u * 720u * 3u;
    ShmCircularBuffer<ShmVideoHeader> consumer(
        "/oe.vid.ingest", kCircularBufferSlotCount, frameBytes, false);

    // Header should be set before pipeline starts (in configureTransport)
    const auto* hdr = consumer.header();
    EXPECT_EQ(hdr->width, 1280u);
    EXPECT_EQ(hdr->height, 720u);
    EXPECT_EQ(hdr->bytesPerPixel, 3u);

    SPDLOG_INFO("shm_header: width={}, height={}, bpp={}",
        hdr->width, hdr->height, hdr->bytesPerPixel);

    // Start and wait for a frame to verify seqNumber increments
    node.onBeforeRun();

    const auto deadline = Clock::now() + std::chrono::seconds(5);
    while (Clock::now() < deadline) {
        if (hdr->seqNumber > 0) break;
        std::this_thread::sleep_for(std::chrono::milliseconds(10));
    }

    EXPECT_GT(hdr->seqNumber, 0u)
        << "No frames written to SHM within 5 seconds";

    node.onAfterStop();
    shm_unlink("/oe.vid.ingest");  // Clean up
}
